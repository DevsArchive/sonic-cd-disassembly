; -------------------------------------------------------------------------
; Align
; -------------------------------------------------------------------------
; PARAMETERS:
;	bound	- Size boundary
; -------------------------------------------------------------------------

align macros bound
	cnop	0,\bound			; Align data

; -------------------------------------------------------------------------
; Push all registers to the stack
; -------------------------------------------------------------------------

pusha macros
	movem.l	d0-a6,-(sp)			; Push registers

; -------------------------------------------------------------------------
; Pop all registers from the stack
; -------------------------------------------------------------------------

popa macros
	movem.l	(sp)+,d0-a6			; Pop registers

; -------------------------------------------------------------------------
; Pad RS to even address
; -------------------------------------------------------------------------

rsEven macros
	rs.b	__rs&1				; Align RS

